    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        LessonsDao lessonsDao = new LessonsDao();
        Lesson lesson = lessonsDao.getLessonByName(jTextField6.getText());

        if (lesson == null) {
            JOptionPane.showMessageDialog(null, "The name you entered was not found on the database. Please try again!");
        } else {
            int returnValue = 0;
            returnValue = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Are you sure?", JOptionPane.YES_NO_OPTION);
            if (returnValue == JOptionPane.YES_OPTION) {
                int status = lessonsDao.deleteLesson(lesson.getSubjectName());
                if (status != 0) {
                    JOptionPane.showMessageDialog(null, "Record successfully deleted from the Database");
                    jTextField6.setText("");
                }
                else{
                     JOptionPane.showMessageDialog(null, "Try Agian !!!");
                    jTextField6.setText("");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

