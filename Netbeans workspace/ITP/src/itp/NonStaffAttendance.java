    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        dbConnection db = new dbConnection();
        Connection conn = db.getConnection();
        
        String nid1 = nid.getText();
        String tin1 = tin.getText();
        String tout1 = tout.getText();
        String leave1 = (String)leave.getSelectedItem();
        
        if(nid1.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter a Student ID!!");
        }
        else if(tin1.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter a Date!");
        }
        else if(tout1.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter a Guardian Name!!");
        }
        else{
        String sql = "Insert into attendanceEmp(date, empID, time_in, time_out, leave) values(?,?,?,?,?)";
        
        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, new SimpleDateFormat("MMM d, yyyy").format(datePicker.getDate()) );
            pst.setString(2, nid.getText());
            pst.setString(3, tin.getText());
            pst.setString(4, tout.getText());
            pst.setString(5, (String) leave.getSelectedItem());
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successfully inserted");
            
        } catch (SQLException ex) {
            Logger.getLogger(NonStaffAttendance.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_submit_btnActionPerformed

