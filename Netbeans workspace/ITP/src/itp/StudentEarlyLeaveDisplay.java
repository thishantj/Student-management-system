    private void dateKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dateKeyReleased
        // TODO add your handling code here:
        //String PATTERN = "^(0?[1-9]|[12][0-9]|3[01])[\\/\\-](0?[1-9]|1[012])[\\/\\-]\\d{4}$";
        String PATTERN1 = "^\\d{4}[\\/\\-](0?[1-9]|1[012])[\\/\\-](0?[1-9]|[12][0-9]|3[01])$";
        
        Pattern patt = Pattern.compile(PATTERN1);
        Matcher match = patt.matcher(date.getText());
        if(!match.matches()){
            dateCh.setText("Incorrect Date(example-2019-02-03)");
        }
        else{
            dateCh.setText(null);
        }
    }//GEN-LAST:event_dateKeyReleased

