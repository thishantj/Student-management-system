    public ArrayList<User> userList() throws SQLException{
        ArrayList<User> userList = new ArrayList<>();
        
        dbConnection db = new dbConnection();
        Connection conn = db.getConnection();
        
        String query1 = "SELECT * FROM studentEarlyLeave";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(query1);
        User user;
        
        while(rs.next()){
            user = new User(rs.getString("studentID"), rs.getString("date"), rs.getString("guardianName"), rs.getString("nic"), rs.getString("contactNo"),rs.getString("address"),rs.getString("reason"));
            userList.add(user);
        }
        
        
        return userList;
        
    }
    
