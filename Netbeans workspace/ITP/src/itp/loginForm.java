    private void btn_Login1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Login1ActionPerformed

        try {
            
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            
            Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456");

            String sql = "Select * from users where username=? and password=? and type=?";
            
            PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, username.getText() );
            pst.setString(2, password.getText() );
            pst.setString(3, type.getSelectedItem().toString() );
            
            ResultSet rs =  pst.executeQuery();
            
            if(rs.next())
            {
                if(type.getSelectedItem().toString().equalsIgnoreCase("administrator") )
                {         
                    homePage home = new homePage();
                    home.setVisible(true); 
                    this.setVisible(false);
                }
                else
                {
                    teacherHomePage thome = new teacherHomePage();
                    thome.setVisible(true); 
                    this.setVisible(false);
                }  
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Username or password is incorrect");
                
                username.setText("");
                password.setText("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(loginForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }

    }//GEN-LAST:event_btn_Login1ActionPerformed

