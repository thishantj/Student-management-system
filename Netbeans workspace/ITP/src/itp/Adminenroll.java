    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        
            if(username.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(null, "Please enter username");
            }
                
            else if (password.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(null, "Please enter password");
            }
            
         else{
        

        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            //String url="jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456";
            Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456");
            
            String query = "insert into users(username, password, type)values(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            
            pst.setString(1, username.getText());
            pst.setString(2, password.getText());
            String type1;
            type1=type.getSelectedItem().toString();
            pst.setString(4, type1);
            
            

            pst.executeUpdate();
            System.out.println("Inserted Sucessfully");
        }
        catch (ClassNotFoundException | SQLException ex){
            System.out.println(ex.getMessage());
                
            }
        }
    }//GEN-LAST:event_saveActionPerformed

