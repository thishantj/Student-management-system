    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        
        try{
                    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                    Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456");

                    int row = TI.getSelectedRow();
            
                    String value = ( TI.getModel().getValueAt(row, 0).toString() );
                    String query = "update notice set name=?, details=?, email=?, date=? where subject="+value;
                    
                    PreparedStatement pst = con.prepareStatement(query);

                    //pst.setString(1, subject.getText());
                    pst.setString(1, name.getText());
                    pst.setString(2, details.getText());
                    pst.setString(3, email.getText());
                    pst.setString(4, new SimpleDateFormat("MMM d, yyyy").format(date.getDate()));

                    pst.executeUpdate();
                    DefaultTableModel model = (DefaultTableModel)TI.getModel();
            
                    model.setRowCount(0);
                    show_Addnoticej();
            
                    JOptionPane.showMessageDialog(null, "Successfully updated");

                }
                catch (Exception ex){
                    System.out.println(ex.getMessage());

                }
    }//GEN-LAST:event_btn_updateActionPerformed

