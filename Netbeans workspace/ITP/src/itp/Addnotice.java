   public ArrayList<Addnoticej> noticeList() {
              ArrayList<Addnoticej> noticeList = new ArrayList<>();
        
        try{
             Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url ="jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456";
            Connection conn = DriverManager.getConnection(url); 
            String query1 = "SELECT * FROM notice";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query1);
            Addnoticej notice;
            
            while(rs.next()){
                  notice = new Addnoticej(rs.getString("subject"),rs.getString("name"),rs.getString("details"),rs.getString("email"),rs.getString("date"));
                
                noticeList.add(notice);
                    }                        
                    }catch(Exception e){
                            JOptionPane.showMessageDialog(null,e);
                    }
        return noticeList;
    }
     
