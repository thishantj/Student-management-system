    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try{
            Connection con = null;
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String connectionURL= "jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456";
        
            con = DriverManager.getConnection(connectionURL);
            
            
        
            String query; 
            query = "delete from progressCard where st_id = ? AND class = ? AND term=? ";
            
            
            PreparedStatement pst = con.prepareStatement(query);
            
            pst.setString(1, jStudentId.getText());
            pst.setString(2, class_select.getSelectedItem().toString());
            pst.setString(3, term_select.getSelectedItem().toString());
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Record deleted");
            
            jStudentId.setText("");
            class_select.setSelectedIndex(0);
            term_select.setSelectedIndex(0);
            
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

