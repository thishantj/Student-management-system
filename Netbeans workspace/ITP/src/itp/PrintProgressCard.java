    private void jIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jIDActionPerformed
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            
            Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=tinytot;user=admin;password=123456");

            String query;
            query = "select * from students where studentID = ?";

            PreparedStatement pst = con.prepareStatement(query);

            pst.setString(1, jID.getText());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){

                jName.setText(rs.getString("fname"));            
                       
            }else{
                JOptionPane.showMessageDialog(null, "Invalid student id");
                jID.setText("");
            }

            

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jIDActionPerformed

